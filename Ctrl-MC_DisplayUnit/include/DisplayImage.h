    private:

        void image(const unsigned char img[], ImagePosition imgPos, uint8_t width, uint8_t height) {
            if (imgPos == imgPosMenuCenter)
                u8g2.drawXBMP((Config::DisplaySettings::screenWidth/2)-(width/2), 3+25-(height/2), width, height, img);
            else if (imgPos == imgPosCenter)
                u8g2.drawXBMP((Config::DisplaySettings::screenWidth/2)-(width/2), (Config::DisplaySettings::screenHeight/2)-(height/2), width, height, img);
            else if (imgPos == imgPos2Left)
                u8g2.drawXBMP(0, (Config::DisplaySettings::screenHeight/2)-(height/2), width, height, img);
            else if (imgPos == imgPos2Right)
                u8g2.drawXBMP(Config::DisplaySettings::screenWidth-width, (Config::DisplaySettings::screenHeight/2)-(height/2), width, height, img);
            else if (imgPos == imgPos3Left)
                u8g2.drawXBMP(((Config::DisplaySettings::screenWidth-35)/2)-(width/2), (Config::DisplaySettings::screenHeight/2)-(height/2), width, height, img);
            else if (imgPos == imgPos3RightTop)
                u8g2.drawXBMP(Config::DisplaySettings::screenWidth-width, 0, width, height, img);
            else if (imgPos == imgPos3RightBottom)
                u8g2.drawXBMP(Config::DisplaySettings::screenWidth-width, Config::DisplaySettings::screenHeight/2, width, height, img);
        }

